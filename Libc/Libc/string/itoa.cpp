#include <stdio.h>
#include <stdlib.h>
#include <string.h>

void itoa(char *buf, unsigned long int n, int base)
{
    unsigned long int tmp = n;
    int i = 0, j;

    do {
        tmp = n % base;
        buf[i++] = (tmp < 10) ? (tmp + '0') : (tmp + 'a' - 10);
    } while (n /= base);
    buf[i--] = 0;

    for (j = 0; j < i; j++, i--) {
        tmp = buf[j];
        buf[j] = buf[i];
        buf[i] = tmp;
    }
}
